                 <article id="conteudo" class="xLinha">
                    <div class="xCentraSite">
                        <div>
                            
                            <h1 class="xTitulo">O que vou aprender neste curso de TI?</h1>
                            
                            <p class="xTxt">Para desenvolver um sistema de informação, temos diversas frentes para o uso do mesmo como por exemplo: <strong>desktop</strong>, que são programas instalados no PC ou notebook, e <strong>sistema para WEB</strong>, que pode ser acessado através de um Navegador como por exemplo, Google Chrome, Internet Explorer ou Mozilla.</p>
                    
                            <p class="xTxt">Neste treinamento, você aprenderá a criar um sistema voltado para WEB, no qual poderá ser acessado tanto no computador, quanto em celulares ou tablets. A <strong>linguagem de programação</strong> que utilizaremos é o <strong>PHP</strong>, que é uma das linguagens de programação mais usadas no mercado mundial de desenvolvimento de sistemas Web.</p>

                            <p class="xTxt">Para abrangermos os 4 pilares de um desenvolvimento de sistemas: <strong class="xTxtMaiusculo">análise, front-end, back-end e banco de dados</strong>, o curso será dividido por módulos da seguinte maneira:</p>
                            
                            <ul class="xLista">
                                <li><h3>Análise do sistema</h3>
                                    <p>Neste módulo, iremos avaliar o projeto a ser construído - quais campos de cada tela, os cuidados que devemos ter com certas informações para desenvolver um sistema, etc.</p>
                                </li>
                                <li><h3>Montagem do Front-End</h3>
                                    <p>Junto com o módulo anterior, serão montadas todas as páginas HTMLs do projeto que será construído. Com o conhecimento adquirido neste módulo, o aluno conseguirá baixar templates bootstraps e montar qualquer layout para construção de sites dinâmicos (sites que envolvem programação) e estáticos (somente HTML)</p>
                                </li>
                                <li><h3>Lógica de programação Estruturada</h3>
                                    <p>Pausaremos o projeto até então construído, para desenvolvermos a lógica de programação e conhecer as sintaxes da linguagem. Serão realizados exercícios explorando vários recursos da programação (for, if..else, switchcase, array, entre outros)</p>
                                </li>
                                <li><h3>Lógica de programação e Orientação a objeto</h3>
                                    <p>Dando a sequência à lógica de programação, neste módulo apresentaremos a programação orientada a objeto (POO).</p>
                                </li>
                                <li><h3>JavaScript e JQuery</h3>
                                    <p>A partir do momento que todas as páginas já foram criadas, neste módulo o aluno aprenderá a validar os campos obrigatórios através do JQuery/JavaScript. O mais bacana é: sabendo todos esses conceitos de lógica e utilizando uma linguagem de programação, quando for necessário migrar para uma outra linguagem de programação, a adaptação será bem mais fácil e ágil!</p>
                                </li>
                                <li><h3>Banco de Dados: Modelagem do Banco de Dados (MySql) + SQL</h3>
                                    <p>Neste módulo, retornaremos na construção de nosso projeto e construiremos o banco de dados do mesmo. E logo na sequencia, passaremos todas as instruções SQL do banco (programação dentro do banco de dados).</p>
                                </li>
                                <li><h3>Conclusão do projeto</h3>
                                    <p>Nessa última fase, aplicaremos os conceitos e práticas dos módulos de lógica para a finalização do projeto, através da programação orientada a objeto, onde criaremos as classes de comunicação com o Banco de Dados, interligando-as com as telas do sistema.</p>
                                </li>
                                <li><h3>Publicação nas nuvens</h3>
                                    <p>Aqui, o aluno aprenderá como contratar um domínio, que é o nome do site na internet (meu_sistema.com.br). Também fará a contratação de uma hospedagem, o que possibilitará deixar seu site já disponível para todos acessarem em qualquer lugar do mundo</p>
                                </li>
                            </ul>
                            
